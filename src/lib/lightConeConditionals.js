export const LightConeConditionals = {
  get: (request) => {
    const lcFn = lightConeOptionMapping[request.lightCone]
    if (!lcFn) {
      return {
        content: () => [],
        teammateContent: () => [],
        defaults: () => ({}),
        teammateDefaults: () => ({}),
      }
    }
    return lcFn(request.lightConeSuperimposition - 1)
  },
}
